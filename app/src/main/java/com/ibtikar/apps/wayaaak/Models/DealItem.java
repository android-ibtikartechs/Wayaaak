package com.ibtikar.apps.wayaaak.Models;

public class DealItem {
    int dealid;
    String dealname, dealcost, dealquantity;

    public int getDealid() {
        return dealid;
    }

    public void setDealid(int dealid) {
        this.dealid = dealid;
    }

    public String getDealname() {
        return dealname;
    }

    public void setDealname(String dealname) {
        this.dealname = dealname;
    }

    public String getDealcost() {
        return dealcost;
    }

    public void setDealcost(String dealcost) {
        this.dealcost = dealcost;
    }

    public String getDealquantity() {
        return dealquantity;
    }

    public void setDealquantity(String dealquantity) {
        this.dealquantity = dealquantity;
    }
}
